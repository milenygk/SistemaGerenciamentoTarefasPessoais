     private Boolean existeDadosTemporarios(){        
        if(DadosTemporarios.tempObject instanceof ModPlanejamentoDiario){
            int id = ((ModPlanejamentoDiario) DadosTemporarios.tempObject).getId();
            Date data = ((ModPlanejamentoDiario) DadosTemporarios.tempObject).getData();
            String rotinaManha = ((ModPlanejamentoDiario) DadosTemporarios.tempObject).getRotinaManha();
            String rotinaTarde = ((ModPlanejamentoDiario) DadosTemporarios.tempObject).getRotinaTarde();
            String rotinaNoite = ((ModPlanejamentoDiario) DadosTemporarios.tempObject).getRotinaNoite();
            String tarfasPontuais = ((ModPlanejamentoDiario) DadosTemporarios.tempObject).getTarfasPontuais(); 
            
            tfId.setText(String.valueOf(id));
            tfData.setText(String.valueOf(String.valueOf(data)));
            taRotinaManha.setText(String.valueOf(rotinaManha));
            taRotinaTarde.setText(rotinaTarde);
            taRotinaNoite.setText(String.valueOf(rotinaNoite));
            taTarfasPontuais.setText(String.valueOf(tarfasPontuais));

            DadosTemporarios.tempObject = null;
            
            return true;
        }else
            return false;
        }
     
